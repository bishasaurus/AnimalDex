package AnimalDex;

public class SimpleAnimal {

	String animalName;
	String animalSpecies;
	String animalColor;

	SimpleAnimal(String aAnimalName, String aAnimalSpecies, String aAnimalColor) {
		this.animalName = aAnimalName;
		this.animalSpecies = aAnimalSpecies;
		this.animalColor = aAnimalColor;
	}

}